package if_statement;

public class If_Statement {

	public static void main(String[] args) {

		int a = 5;

		if (a != 8) {

			System.out.println(a);
		}

		System.out.println("--------------------------------------");

		long b = 67;
		if (b == 67) {
			System.out.println(b);

		}

		System.out.println("-------------------------------");

		int person_age = 20;
		if (person_age > 18) {

			System.out.println("Eligible for vote");
		}

		System.out.println("------------------------------------");

		String str = "Pallavi";

		if (str == "Pallavi") {

			System.out.println(str);
		}

		System.out.println("---------------------------------");

		float f = 25.6f;

		if (f > 23.6f) {

			System.out.println(f);
		}

		System.out.println("----------------------------------------------");

		int d = 156;
		if (d >= 101) {

			System.out.println(d);
		}
	}

}
