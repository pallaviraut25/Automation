package loopingstatement;

public class DoWhileLoop {

	public static void main(String[] args) {

		// 1 to 10

		int a = 1;
		do {
			System.out.print(a + " ");
			a++;
			

		} while (a <= 10);
		
	
		System.out.println();
		System.out.println("------------------------------------");

		int b =10;
		do {
			System.out.print(b + " ");
			b--;
			
		} while (b >=1);
	}

}
